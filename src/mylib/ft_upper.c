#include <stddef.h>

char	*ft_upper(char *src)
{
	size_t	i;

	i = 0;
	while (src[i])
	{
		if (src[i] >= 'a' && src[i] <= 'z')
			src[i] += 'A' - 'a';
		++i;
	}
	return (src);
}
